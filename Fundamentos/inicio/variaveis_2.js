// Essiste varios tipos de variaves, em outras linguagens, você teria que colocar o tipo dela antes de por o nome, ja aqui em JS, você não precisa colocar o tipo dela, pois ela atribuira um tipo automaticamente a partir do valor que vou atribuido a ela.

let idade = 31;
console.log(typeof idade);

let salario = 4578.32;
console.log(typeof salario);

let estaChovendo = true; // ou false
console.log(typeof estaChovendo);

console.log(typeof "Teste");